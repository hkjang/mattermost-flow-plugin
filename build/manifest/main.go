package main

import (
	"archive/tar"
	"compress/gzip"
	"encoding/json"
	"fmt"
	"io"
	"net/url"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/mattermost/mattermost/server/public/model"
	"github.com/pkg/errors"
)

const pluginIDGoFileTemplate = `// This file is automatically generated. Do not modify it manually.

package main

import (
	"encoding/json"
	"strings"

	"github.com/mattermost/mattermost/server/public/model"
)

var manifest *model.Manifest

const manifestStr = ` + "`" + `
%s
` + "`" + `

func init() {
	_ = json.NewDecoder(strings.NewReader(manifestStr)).Decode(&manifest)
}
`

const pluginIDJSFileTemplate = `// This file is automatically generated. Do not modify it manually.

const manifest = JSON.parse(` + "`" + `
%s
` + "`" + `);

export default manifest;
`

// These build-time vars are read from shell commands and populated in ../setup.mk
var (
	BuildHashShort  string
	BuildTagLatest  string
	BuildTagCurrent string
)

func main() {
	if len(os.Args) <= 1 {
		panic("no cmd specified")
	}

	manifest, err := findManifest()
	if err != nil {
		panic("failed to find manifest: " + err.Error())
	}

	cmd := os.Args[1]
	switch cmd {
	case "id":
		dumpPluginID(manifest)

	case "version":
		dumpPluginVersion(manifest)

	case "has_server":
		if manifest.HasServer() {
			fmt.Printf("true")
		}

	case "has_webapp":
		if manifest.HasWebapp() {
			fmt.Printf("true")
		}

	case "apply":
		if err := applyManifest(manifest); err != nil {
			panic("failed to apply manifest: " + err.Error())
		}

	case "dist":
		if err := distManifest(manifest); err != nil {
			panic("failed to write manifest to dist directory: " + err.Error())
		}

	case "bundle":
		if len(os.Args) < 4 {
			panic("bundle requires plugin id and bundle name")
		}
		if err := bundlePlugin(os.Args[2], os.Args[3]); err != nil {
			panic("failed to bundle plugin: " + err.Error())
		}

	case "check":
		if err := manifest.IsValid(); err != nil {
			panic("failed to check manifest: " + err.Error())
		}

	default:
		panic("unrecognized command: " + cmd)
	}
}

func findManifest() (*model.Manifest, error) {
	_, manifestFilePath, err := model.FindManifest(".")
	if err != nil {
		return nil, errors.Wrap(err, "failed to find manifest in current working directory")
	}
	manifestFile, err := os.Open(manifestFilePath) //nolint:gosec
	if err != nil {
		return nil, errors.Wrapf(err, "failed to open %s", manifestFilePath)
	}
	defer func() { _ = manifestFile.Close() }()

	// Re-decode the manifest, disallowing unknown fields. When we write the manifest back out,
	// we don't want to accidentally clobber anything we won't preserve.
	var manifest model.Manifest
	decoder := json.NewDecoder(manifestFile)
	decoder.DisallowUnknownFields()
	if err = decoder.Decode(&manifest); err != nil {
		return nil, errors.Wrap(err, "failed to parse manifest")
	}

	// If no version is listed in the manifest, generate one based on the state of the current
	// commit, and use the first version we find (to prevent causing errors)
	if manifest.Version == "" {
		var version string
		tags := strings.FieldsSeq(BuildTagCurrent)
		for t := range tags {
			if strings.HasPrefix(t, "v") {
				version = t
				break
			}
		}
		if version == "" {
			if BuildTagLatest != "" {
				version = BuildTagLatest + "+" + BuildHashShort
			} else {
				version = "v0.0.0+" + BuildHashShort
			}
		}
		manifest.Version = strings.TrimPrefix(version, "v")
	}

	// If no release notes specified, generate one from the latest tag, if present.
	if manifest.ReleaseNotesURL == "" && BuildTagLatest != "" {
		releaseNotesURL, err := url.JoinPath(manifest.HomepageURL, "releases", "tag", BuildTagLatest)
		if err != nil {
			return nil, errors.Wrap(err, "failed to build release notes URL")
		}
		manifest.ReleaseNotesURL = releaseNotesURL
	}

	return &manifest, nil
}

// dumpPluginId writes the plugin id from the given manifest to standard out
func dumpPluginID(manifest *model.Manifest) {
	fmt.Printf("%s", manifest.Id)
}

// dumpPluginVersion writes the plugin version from the given manifest to standard out
func dumpPluginVersion(manifest *model.Manifest) {
	fmt.Printf("%s", manifest.Version)
}

// applyManifest propagates the plugin_id into the server and webapp folders, as necessary
func applyManifest(manifest *model.Manifest) error {
	if manifest.HasServer() {
		// generate JSON representation of Manifest.
		manifestBytes, err := json.MarshalIndent(manifest, "", "  ")
		if err != nil {
			return err
		}
		manifestStr := string(manifestBytes)

		// write generated code to file by using Go file template.
		if err := os.WriteFile(
			"server/manifest.go",
			fmt.Appendf(nil, pluginIDGoFileTemplate, manifestStr),
			0o600,
		); err != nil {
			return errors.Wrap(err, "failed to write server/manifest.go")
		}
	}

	if manifest.HasWebapp() {
		// generate JSON representation of Manifest.
		// JSON is very similar and compatible with JS's object literals. so, what we do here
		// is actually JS code generation.
		manifestBytes, err := json.MarshalIndent(manifest, "", "    ")
		if err != nil {
			return err
		}
		manifestStr := string(manifestBytes)

		// Escape newlines
		manifestStr = strings.ReplaceAll(manifestStr, `\n`, `\\n`)

		// write generated code to file by using JS file template.
		if err := os.WriteFile(
			"webapp/src/manifest.ts",
			fmt.Appendf(nil, pluginIDJSFileTemplate, manifestStr),
			0o600,
		); err != nil {
			return errors.Wrap(err, "failed to open webapp/src/manifest.ts")
		}
	}

	return nil
}

// distManifest writes the manifest file to the dist directory
func distManifest(manifest *model.Manifest) error {
	manifestBytes, err := json.MarshalIndent(manifest, "", "    ")
	if err != nil {
		return err
	}

	if err := os.WriteFile(fmt.Sprintf("dist/%s/plugin.json", manifest.Id), manifestBytes, 0o600); err != nil {
		return errors.Wrap(err, "failed to write plugin.json")
	}

	return nil
}

func bundlePlugin(pluginID, bundleName string) error {
	rootPath := filepath.Join("dist", pluginID)
	archivePath := filepath.Join("dist", bundleName)

	archiveFile, err := os.Create(archivePath)
	if err != nil {
		return errors.Wrap(err, "failed to create bundle archive")
	}
	defer func() { _ = archiveFile.Close() }()

	gzipWriter := gzip.NewWriter(archiveFile)
	defer func() { _ = gzipWriter.Close() }()

	tarWriter := tar.NewWriter(gzipWriter)
	defer func() { _ = tarWriter.Close() }()

	entries, err := collectBundleEntries(rootPath)
	if err != nil {
		return err
	}

	for _, entryPath := range entries {
		info, statErr := os.Stat(entryPath)
		if statErr != nil {
			return errors.Wrapf(statErr, "failed to stat %s", entryPath)
		}

		relativePath, relErr := filepath.Rel("dist", entryPath)
		if relErr != nil {
			return errors.Wrapf(relErr, "failed to compute archive path for %s", entryPath)
		}

		header, headerErr := tar.FileInfoHeader(info, "")
		if headerErr != nil {
			return errors.Wrapf(headerErr, "failed to create header for %s", entryPath)
		}
		header.Name = filepath.ToSlash(relativePath)
		header.Mode = normalizedBundleMode(header.Name, info)

		if err := tarWriter.WriteHeader(header); err != nil {
			return errors.Wrapf(err, "failed to write header for %s", entryPath)
		}

		if info.IsDir() {
			continue
		}

		file, openErr := os.Open(entryPath) //nolint:gosec
		if openErr != nil {
			return errors.Wrapf(openErr, "failed to open %s", entryPath)
		}

		if _, copyErr := io.Copy(tarWriter, file); copyErr != nil {
			_ = file.Close()
			return errors.Wrapf(copyErr, "failed to write file %s", entryPath)
		}

		if closeErr := file.Close(); closeErr != nil {
			return errors.Wrapf(closeErr, "failed to close %s", entryPath)
		}
	}

	return nil
}

func collectBundleEntries(rootPath string) ([]string, error) {
	entries := []string{rootPath}
	if _, err := os.Stat(rootPath); err != nil {
		return nil, errors.Wrap(err, "failed to find bundle root")
	}

	if err := filepath.Walk(rootPath, func(path string, info os.FileInfo, walkErr error) error {
		if walkErr != nil {
			return walkErr
		}
		if path == rootPath {
			return nil
		}
		entries = append(entries, path)
		return nil
	}); err != nil {
		return nil, errors.Wrap(err, "failed to collect bundle entries")
	}

	sort.Strings(entries)
	return entries, nil
}

func normalizedBundleMode(headerName string, info os.FileInfo) int64 {
	if info.IsDir() {
		return 0o755
	}

	if strings.Contains(headerName, "/server/dist/") {
		return 0o755
	}

	return 0o644
}
